'use strict';

const credentialStore  = require('../credential-store');
const emailGateway     = require('../email-gateway');
const { getConfig }    = require('../../config/cosa.config');
const { createLogger } = require('../logger');

const log = createLogger('token-rotation-remind');

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const NAME       = 'token_rotation_remind';
const RISK_LEVEL = 'read';

/**
 * Rotation policy (in days).
 * SSH authorized keys have no fixed expiry; an annual review is flagged instead.
 */
const ROTATION_POLICIES = [
  { name: 'clover_api_key',    label: 'Clover API Key',         maxAgeDays: 180 },
  { name: 'jwt_secret',        label: 'JWT Secret',             maxAgeDays: 90  },
  { name: 's3_access_key',     label: 'S3 Access Key',          maxAgeDays: 90  },
  { name: 'ssh_authorized_key',label: 'SSH Authorized Keys',    maxAgeDays: 365 },
];

const INPUT_SCHEMA = {
  type:                 'object',
  properties:           {},
  required:             [],
  additionalProperties: false,
};

const SCHEMA = {
  description:
    'Check whether Clover API tokens and other rotatable credentials are due ' +
    'for rotation, and send operator reminder emails for any that are overdue. ' +
    'Rotation policy: Clover API key (6 months), JWT secret (90 days), ' +
    'S3 access key (90 days), SSH authorized keys (annual review). ' +
    'Credentials absent from the store are skipped with a warning. ' +
    'Returns a list of checked credentials and whether an email was sent.',
  inputSchema: INPUT_SCHEMA,
};

// ---------------------------------------------------------------------------
// Email formatter
// ---------------------------------------------------------------------------

/**
 * Build the plain-text email body for rotation reminders.
 *
 * @param {{
 *   appName: string,
 *   dueItems: Array<{ label: string, ageDays: number, maxAgeDays: number, lastRotated: string }>,
 * }} params
 * @returns {string}
 */
function buildEmailBody({ appName, dueItems }) {
  const lines = [];

  lines.push('╔══════════════════════════════════════════════════════╗');
  lines.push('║  COSA — CREDENTIAL ROTATION REMINDER                 ║');
  lines.push('╚══════════════════════════════════════════════════════╝');
  lines.push('');
  lines.push(`Appliance : ${appName}`);
  lines.push(`Issued At : ${new Date().toISOString()}`);
  lines.push('');
  lines.push(
    `The following ${dueItems.length} credential(s) are due for rotation:`
  );
  lines.push('');

  for (const item of dueItems) {
    const overdue = item.ageDays - item.maxAgeDays;
    lines.push(`  • ${item.label}`);
    lines.push(`    Last rotated : ${item.lastRotated}`);
    lines.push(`    Age          : ${item.ageDays} days (limit: ${item.maxAgeDays} days)`);
    lines.push(`    Overdue by   : ${overdue} day(s)`);
    lines.push('');
  }

  lines.push('──────────────────────────────────────────────────────────');
  lines.push('Please rotate the credentials above at your earliest convenience.');
  lines.push('This reminder was generated by COSA (Compute-Oriented System Agent).');

  return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Handler
// ---------------------------------------------------------------------------

/**
 * @returns {Promise<{
 *   checked: Array<{ credential: string, label: string, present: boolean,
 *                    ageDays: number|null, maxAgeDays: number, dueForRotation: boolean }>,
 *   dueCount: number,
 *   emailSent: boolean,
 *   checked_at: string,
 * }>}
 */
async function handler() {
  const checked_at = new Date().toISOString();

  const { appliance } = getConfig();
  const appName        = appliance.appliance?.name ?? 'COSA';
  const operatorEmail  = appliance.operator?.email;

  // ── 1. Query credential store metadata ────────────────────────────────────
  //
  // We use getMetadata() (not get()) so that the credential value is never
  // loaded into memory.  Only created_at is used to determine rotation age.
  const checked  = [];
  const dueItems = [];

  for (const policy of ROTATION_POLICIES) {
    const meta = credentialStore.getMetadata(policy.name);

    if (meta === null) {
      log.warn(
        `token_rotation_remind: credential "${policy.name}" not found in store — skipping`
      );
      checked.push({
        credential:      policy.name,
        label:           policy.label,
        present:         false,
        ageDays:         null,
        maxAgeDays:      policy.maxAgeDays,
        dueForRotation:  false,
      });
      continue;
    }

    const lastRotatedEpoch = meta.created_at;
    const ageDays = Math.floor(
      (Date.now() - lastRotatedEpoch) / (1000 * 60 * 60 * 24)
    );
    const dueForRotation = ageDays >= policy.maxAgeDays;
    const lastRotated    = new Date(lastRotatedEpoch).toISOString();

    log.info(
      `token_rotation_remind: "${policy.name}" ageDays=${ageDays} ` +
      `maxAgeDays=${policy.maxAgeDays} dueForRotation=${dueForRotation}`
    );

    checked.push({
      credential:     policy.name,
      label:          policy.label,
      present:        true,
      ageDays,
      maxAgeDays:     policy.maxAgeDays,
      dueForRotation,
    });

    if (dueForRotation) {
      dueItems.push({ label: policy.label, ageDays, maxAgeDays: policy.maxAgeDays, lastRotated });
    }
  }

  // ── 2. Send reminder email if any credentials are due ────────────────────
  let emailSent = false;

  if (dueItems.length > 0) {
    if (!operatorEmail) {
      log.warn(
        'token_rotation_remind: operator.email not configured — ' +
        `skipping email for ${dueItems.length} due credential(s)`
      );
    } else {
      const subject =
        `[COSA] Credential Rotation Reminder — ${dueItems.length} credential(s) due`;
      const body = buildEmailBody({ appName, dueItems });

      try {
        await emailGateway.sendEmail({ to: operatorEmail, subject, text: body });
        emailSent = true;
        log.info(
          `token_rotation_remind: reminder email sent to ${operatorEmail} ` +
          `(${dueItems.length} credential(s) due)`
        );
      } catch (err) {
        log.warn(`token_rotation_remind: email send failed — ${err.message}`);
      }
    }
  } else {
    log.info('token_rotation_remind: all credentials within rotation window — no email sent');
  }

  return {
    checked,
    dueCount:  dueItems.length,
    emailSent,
    checked_at,
  };
}

// ---------------------------------------------------------------------------
// Exports
// ---------------------------------------------------------------------------

module.exports = { name: NAME, schema: SCHEMA, handler, riskLevel: RISK_LEVEL };
